<?php
$_tests_dir = getenv( 'WP_TESTS_DIR' );
if ( ! $_tests_dir ) {
    $_tests_dir = rtrim( sys_get_temp_dir(), '/\' ) . '/wordpress-tests-lib';
}

if ( ! file_exists( $_tests_dir . '/includes/functions.php' ) ) {
    fwrite( STDERR, "Could not find WordPress tests in \\{$_tests_dir}\\.\n" );
    exit( 1 );
}

require_once $_tests_dir . '/includes/functions.php';

function kh_xapi_tests_bootstrap() {
    require dirname( __DIR__ ) . '/kh-xapi.php';
    kh_xapi()->db()->maybe_upgrade();
}
tests_add_filter( 'muplugins_loaded', 'kh_xapi_tests_bootstrap' );

require $_tests_dir . '/includes/bootstrap.php';
